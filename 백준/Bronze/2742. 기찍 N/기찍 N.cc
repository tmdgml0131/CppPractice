#include <bits/stdc++.h>

using namespace std;

// 첫째줄 시 분 초
// 둘째줄 초단위
// 출력 시 분 초 단위로 언제 끝나는지
int main()
{
    int a;
    cin >> a;
    
    for(a; a>=1; --a)
    {
        cout << a << "\n";
    }
    return 0;
}
